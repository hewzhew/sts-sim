#[derive(Clone, Copy, Debug, PartialEq)]
pub enum CardType {
    Attack,
    Skill,
    Power,
    Status,
    Curse,
}

#[derive(Clone, Copy, Debug, PartialEq)]
pub enum CardRarity {
    Basic,
    Common,
    Uncommon,
    Rare,
    Special,
    Curse,
}

#[derive(Clone, Copy, Debug, PartialEq)]
pub enum CardTarget {
    Enemy,
    AllEnemy,
    SelfAndEnemy,
    SelfTarget,
    None,
}

#[derive(Clone, Copy, Debug, PartialEq)]
pub enum CardTag {
    Strike,
    StarterStrike,
    StarterDefend,
    Healing,
    Empty,
}

pub struct CardDefinition {
    pub id: CardId,
    pub name: &'static str,
    pub card_type: CardType,
    pub rarity: CardRarity,
    pub cost: i8, // -1 for X cost, -2 for unplayable
    pub base_damage: i32,
    pub base_block: i32,
    pub base_magic: i32,
    pub target: CardTarget,
    pub is_multi_damage: bool,
    pub exhaust: bool,
    pub ethereal: bool,
    pub innate: bool,
    pub tags: &'static [CardTag],
    pub upgrade_damage: i32,
    pub upgrade_block: i32,
    pub upgrade_magic: i32,
}

fn build_curse(id: CardId, name: &'static str, cost: i8) -> CardDefinition {
    let mut def = CardDefinition {
        id,
        name,
        card_type: CardType::Curse,
        rarity: CardRarity::Curse,
        cost,
        base_damage: 0,
        base_block: 0,
        base_magic: 0,
        target: CardTarget::None,
        is_multi_damage: false,
        exhaust: false,
        ethereal: false,
        innate: false,
        tags: &[],
        upgrade_damage: 0,
        upgrade_block: 0,
        upgrade_magic: 0,
    };

    if id == CardId::AscendersBane
        || id == CardId::CurseOfTheBell
        || id == CardId::Necronomicurse
        || id == CardId::Pride
    {
        def.rarity = CardRarity::Special;
    }

    if id == CardId::AscendersBane || id == CardId::Clumsy {
        def.ethereal = true;
    }

    if id == CardId::Writhe || id == CardId::Pride {
        def.innate = true;
    }

    if id == CardId::Pride {
        def.exhaust = true;
    }

    def
}

/// Dispatches card-specific logic that natively occurs exactly when a card is exhausted.
/// Base Slay the Spire has only two cards that override `triggerOnExhaust()`:
/// 1. Sentinel (Gain Energy)
/// 2. Necronomicurse (Clone itself back to hand)
pub fn resolve_card_on_exhaust(
    card: &crate::runtime::combat::CombatCard,
    _state: &crate::runtime::combat::CombatState,
) -> Vec<crate::runtime::action::ActionInfo> {
    match card.id {
        CardId::Necronomicurse => vec![crate::runtime::action::ActionInfo {
            action: crate::runtime::action::Action::MakeTempCardInHand {
                card_id: CardId::Necronomicurse,
                amount: 1,
                upgraded: false,
            },
            insertion_mode: crate::runtime::action::AddTo::Bottom,
        }],
        CardId::Sentinel => vec![crate::runtime::action::ActionInfo {
            action: crate::runtime::action::Action::GainEnergy {
                amount: if card.upgrades > 0 { 3 } else { 2 },
            },
            insertion_mode: crate::runtime::action::AddTo::Top,
        }],
        _ => vec![],
    }
}

pub fn is_starter_strike(id: CardId) -> bool {
    matches!(id, CardId::Strike | CardId::StrikeG)
}

pub fn is_starter_defend(id: CardId) -> bool {
    matches!(id, CardId::Defend | CardId::DefendG)
}

pub fn is_starter_basic(id: CardId) -> bool {
    is_starter_strike(id) || is_starter_defend(id)
}

pub fn is_innate_card(card: &crate::runtime::combat::CombatCard) -> bool {
    get_card_definition(card.id).innate
        || matches!(card.id, CardId::Brutality) && card.upgrades > 0
        || matches!(card.id, CardId::AfterImage) && card.upgrades > 0
        || matches!(card.id, CardId::InfiniteBlades) && card.upgrades > 0
}

